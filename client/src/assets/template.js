// Helper functions
export const getTheme = (themeKey) => themes[themeKey];

export const getThemesByCategory = (category) => 
  Object.entries(themes).filter(([_, theme]) => theme.category === category);

export const getAllThemeNames = () => 
  Object.keys(themes);

export const getThemeCategories = () => 
  [...new Set(Object.values(themes).map(theme => theme.category))];

// CSS custom properties generator
export const generateCSSVariables = (themeKey) => {
  const theme = themes[themeKey];
  if (!theme) return '';
  
  return Object.entries(theme.colors)
    .map(([key, value]) => `  --color-${key.replace(/([A-Z])/g, '-$1').toLowerCase()}: ${value};`)
    .join('\n');
};

// Apply theme to document root
export const applyTheme = (themeKey) => {
  const theme = themes[themeKey];
  if (!theme) return;
  
  const root = document.documentElement;
  Object.entries(theme.colors).forEach(([key, value]) => {
    const cssVar = `--color-${key.replace(/([A-Z])/g, '-$1').toLowerCase()}`;
    root.style.setProperty(cssVar, value);
  });
};

// Default export
export default themes;