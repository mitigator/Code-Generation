const mongoose = require('mongoose');

const userSchema = new mongoose.Schema(
  {
  username: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    match: [
      /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/,
      'Please add a valid email',
    ],
  },
  password: {
    type: String,
    required: true,
    minlength: 6,
  },
  role: {
    type: String,
    enum: ['user', 'admin'],
    default: 'user',
  },
  },
  {
    timestamps: true,
  }
);

// Add any methods or middleware here

const User = mongoose.model('User', userSchema);

module.exports = User;